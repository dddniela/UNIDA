        <h2 class="seccion-texto-titulo">Requisitos para la permanencia en el programa</h2>
        <div class="seccion-texto-separador"></div>
        <li class="seccion-texto-lista-item">Acatar las disposiciones reglamentarias de la institución y del Tecnológico Nacional de México para su buen desempeño y mejor aprovechamiento, de lo contrario, estará sujeto a sanciones por las autoridades correspondientes</li>
