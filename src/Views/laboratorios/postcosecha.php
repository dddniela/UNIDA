            <h2 class="seccion-texto-titulo">Laboratorio de Manejo Postcosecha</h2>
            <div class="seccion-texto-separador"></div>
            <p class="seccion-texto-parrafo">Los objetivos son: realizar estudios de almacenamiento post-cosecha de frutos tropicales que involucran tanto la protección al daño por frío como el control de la maduración de los mismos por atmósferas modificadas, productos mínimamente procesados y compuestos antioxidantes en frutos. Se trabaja con especies frutales genéticamente modificadas para obtener una larga vida de anaquel. Asociado a estos proyectos, se cuenta con un invernadero de 50m2 con construcción robusta y medidas de seguridad para alojar plantas modificadas genéticamente. En el mismo laboratorio se tiene una línea sobre química de lípidos que involucra reacciones de síntesis de glicéridos de interés alimentario y nutracéuticos. Se realizan trabajos sobre preparación de nanoemulsiones con lípidos y fosfolípidos modificados, geles y organogeles acarreadores de compuestos bioactivos. Se desarrolola una línea sobre la formación de óxidos de colesterol en alimentos y sus efectos en tejidos animales.</p>

