            <p class="seccion-texto-parrafo">Desarrollar y realizar proyectos de investigación de ciencia básica y aplicada en el ámbito de la ecología e ingeniería ecológica y ambiental y el manejo de recursos naturales, así como de las disciplinas involucradas en los otros dos ejes de la sustentabilidad “Social, Cultural-Económico”. Realizar planes y programas de monitoreo continuo de diferentes ecosistemas costeros. Desarrollar proyectos que contribuyan a proteger la salud y seguridad de la comunidad y del medio ambiente. Fomentar la participación de los estudiantes e investigadores en la resolución de problemas ambientales de manera interdisciplinaria. Fortalecer el desarrollo tecnológico en la medida de nuestras competencias, fomentar la participación de la Institución en la propuesta y toma de decisiones en materia ambiental. Consolidar grupos de investigación básica, aplicada y desarrollo tecnológico en lo concerniente a las Ciencias Socioambientales, vinculando los proyectos con las necesidades del entorno y fortalecer la vinculación con los sectores productivos, a través de proyectos sobre Desarrollo Sostenible.</p>

