            <h2 class="seccion-texto-titulo">Laboratorio de Microbiología</h2>
            <div class="seccion-texto-separador"></div>
            <p class="seccion-texto-parrafo">Los objetivos de este laboratorio son: realizar estudios de investigación sobre microorganismos productores de metabolitos, con características antimicrobianas (bacteriocinas) y su posible uso como bioconservadores, en la elaboración de alimentos. Formulación de empaques activo con polímeros biodegradable y conservadores de origen natural. Se cuenta con la infraestructura necesaria para el cultivo, identificación y conservación de microorganismos (área de siembra : campanas de flujo laminar vertical y horizontal, incubadoras, fermentador, liofilizador, esterilizadora automática, ultracongeladores, lector de microplacas, etc.) así como para la purificación y análisis de sus productos espectrofotómetro, equipo para electroforesis vertical y horizontal, centrifuga refrigerada.</p>

