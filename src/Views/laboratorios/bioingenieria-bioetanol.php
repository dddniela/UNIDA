        <h2 class="seccion-texto-titulo">Laboratorio de Bioingeniería y Planta Piloto de bioetanol 1G y 2G</h2>
        <div class="seccion-texto-separador"></div>
        <p class="seccion-texto-parrafo">Realizamos proyectos de investigación enfocados al desarrollo de nuevos procesos o mejoramiento de procesos biológicos tales como: producción de bioetanol de 1ª y 2ª generación, xilitol, edulcorante de alto valor, ácido acético, láctico, biogás, enzimas hidrolíticas como celulasas , xilanasas y amilasas, así como cultivo de células vegetales en biorreactor para la producción de anticancerígenos; desarrollos de modelos de medición en tiempo real utilizando espectroscopia de infrarrojo cercano (NIRS). Todos los proyectos son realizados primero en el laboratorio en donde se aíslan y seleccionan los microorganismos autóctonos altamente productores, se llevan a cabo optimizaciones de condiciones ambientales y medios de cultivo a nivel matraz y en bioerreactor se realizan fermentaciones en diferentes modos de operación (lote, lote alimentado y continuo). Posteriormente se llevan a cabo las pruebas de escalamiento y validación de resultados obtenidos a nivel laboratorio en la planta piloto de bioetanol.</p>

